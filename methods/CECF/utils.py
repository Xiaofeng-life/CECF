def write_metrics(path, epoch, ssim, psnr, uiqm):
    with open(path, mode='a') as f:
        info = str(epoch) + " " + str(ssim) + " " + str(psnr) + " " + str(uiqm) + "\n"
        f.write(info)
        f.close()


